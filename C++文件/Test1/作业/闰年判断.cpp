#include <stdio.h>
int main(){
	int i;//定义一个变量 
	for (i=2000;i<=2500;i++){//定义一个循环遍历从2000到2500的年份 
		if(i%4==0 && i%100 !=0|| (i%400==0)){// ||前面就是题目所说的两个条件，i能被4整除但是不能被100整除
			printf("%4d 是闰年",i);		// ||后面就是能被400整除条件， 	
		printf("\n");//换行符 
		} 
		else{ 
			printf("%4d 不是闰年",i);
		printf("\n");
		}				
	}
	return 0;
}
